                                        <h3>WHAT IS A CORPORATE CREDIT CARD?
                                        </h3>
                                        <p>Corporate credit cards work similarly to normal credit cards in that they are used to make purchases on credit, which must then be paid off over time or by the next billing cycle. Corporate cards also offer valuable spending rewards just like normal rewards credit cards, but often at higher rates.
                                        </p>
