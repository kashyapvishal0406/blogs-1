                                    <h4><a href="#">How did BJP muff up ‘singhasan’ state? More to it than social engg</a></h4>
                                    <p>wrong bet: Why did BJP’s caste math fail while Akhilesh’s worked? It had a lot to do with candidates and cadre
                                        But how did UP go from BJP’s big strength to its weakness? I posit three reasons for this:

                                        First, INDIA fought this election in an organised fashion, and its tactics of mobilising people by weaving imagined dangers about the Constitution, reservation and other issues to conjure up disquiet among a section of OBC, MBC and Dalits. This sense of insecurity fractured the broader Hindutva identity that had tethered them to BJP for over a decade.

