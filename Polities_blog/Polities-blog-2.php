                                        <p style="text-align:justify !important;">The second reason may be the slow and steady accumulation of small dissatisfactions which were woven in a meta narrative by INDIA bloc by linking them with issues such as unemployment, inflation, Agniveer etc. They also made aggressive use of social media to create an anti-BJP mood in the state.
                                            Third, Samajwadi Party (SP) diversified its social alliance by taking a leaf out of the BJP model of social engineering, which is based on non-Yadav, non-Jatav political participation. They moved from their traditional M-Y (Muslim-Yadav) social engineering and expanded their base among non-Yadav, non-Muslim communities by giving them tickets in this election. For instance, a Dalit candidate (Awadhesh Prasad) was fielded from the Faizabad seat even though it’s not a reserved constituency. Selection was strategic even in neighbouring seats like Sultanpur where a Nishad candidate was fielded, and Ambedkarnagar which had a Kurmi. Add these to the M-Y kitty, and SP had a distinct edge.
                                            But why did BJP’s social engineering fail while INDIA’s worked? One big reason for this is that BJP candidates of these communities were being repeated for the third time, and most of them may have faced anti-incumbency at local level. Moreover, party cadre were not enthused enough to counter these. On the other hand, candidates of INDIA alliance were fresh faces who appealed to voters of OBCs-MBCs and mostly non Jatav Dalit communities. The Akhilesh Yadav-Rahul Gandhi bonhomie also enthused cadres. In the context of BJP, the lack of coordination between candidates and cadres, as well as between BJP and its allies in planning the joint campaign may have also contributed to setbacks in UP.
                                            As for the much-touted Modi magic, it did work impressively well in other parts of the country but in UP, it actually proved to be a disadvantage. Candidates and many cadres became lethargic due to their over-dependence on Modi-Yogi image.
                                            It is interesting to observe that the same methods of campaigning were applied by INDIA alliance and Congress in the other adjoining Hindi belt states of Bihar and MP. But as election results showed, this method of opposition failed and BJP coasted to a very impressive victory in MP, and a strong performance in Bihar. Why is it so? In my view, in Bihar the NDA alliance has big faces of non-Yadav/OBC politics like Nitish Kumar, Samrat Choudhary, and Dalit leader Chirag Paswan from the Dusadh caste which is the largest Dalit community in Bihar. They also have a face like Jitan Ram Manjhi who leads Musahars (rat eaters), another large Dalit community of Bihar. These factors together proved hurdles for INDIA bloc. In MP, BJP has the credible OBC face of Shivraj Singh Chauhan and chief minister Mohan Yadav who countered the Congress narrative. Congress lacked a credible OBC political face in these states.

