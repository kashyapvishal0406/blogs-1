                                        <h3>The Heliosphere: Earth’s Protective Plasma Shield
                                        </h3>
                                        <p style="text-align:justify;">The entire solar system is enveloped in a protective plasma shield known as the heliosphere, which emanates from the sun. It consists of a constant flow of charged particles, called solar wind, that stretch well past Pluto, wrapping the planets in what NASA calls a “giant bubble.” This shield protects us from radiation and galactic rays that could alter DNA, and scientists believe it’s part of the reason life evolved on Earth as it did. According to the latest paper, the cold cloud compressed the heliosphere in such a way that it briefly placed Earth and the other planets outside of the heliosphere’s influence.

                                            “This paper is the first to quantitatively show there was an encounter between the sun and something outside of the solar system that would have affected Earth’s climate,” says Opher, an expert on the heliosphere whose models have significantly shaped our scientific understanding of the bubble and how it is structured by the solar wind pushing up against the interstellar medium.
                                        </p>
                                        <h3>Tracing the Sun’s Path Through the Galaxy
                                        </h3>
                                        <p style="text-align:justify;">To study this phenomenon, Opher and her collaborators essentially looked back in time, using sophisticated computer models to visualize where the sun was positioned two million years in the past, along with the heliosphere and the rest of the solar system. They also mapped the path of the Local Ribbon of Cold Clouds system, a string of large, dense, very cold clouds mostly made of hydrogen atoms. Their simulations showed that one of the clouds close to the end of that ribbon, named the Local Lynx of Cold Cloud, could have collided with the heliosphere.

                                            If that had happened, says Opher, Earth would have been fully exposed to the interstellar medium, where gas and dust mix with the leftover atomic elements of exploded stars, including iron and plutonium. Normally, the heliosphere filters out most of these radioactive particles. But without protection, they can easily reach Earth. According to the paper, this aligns with geological evidence that shows increased 60Fe (iron 60) and 244Pu (plutonium 244) isotopes in the ocean, on the moon, Antarctic snow, and ice cores from the same time period. The timing also matches with temperature records that indicate a cooling period.
                                        </p>
