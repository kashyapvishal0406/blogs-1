                                        <h3>How do people get infected by Salmonella?</h3>
                                        <p>Many of the foods Salmonella bacteria lurk in are raw or undercooked. Breaded raw chicken products like nuggets and chicken Kiev are one way it may reach your table. But a variety of foods have been implicated — including organic basil, cantaloupes, ground beef, nut butters, raw cookie dough, eggs, raw or unpasteurized milk, and flour.
                                        </p>
                                        <ul class="layout-link">
                                            <li>Even small pets such as turtles and frogs, along with dog food, have contributed to Salmonella outbreaks in recent years.</li>
                                            <li>You can also become infected by handling contaminated food and spreading the bacteria from your hands to your mouth. Additionally, you can spread it to others on your hands or even on your clothes without becoming sick yourself.</li>


