                                <h3>What symptoms can Salmonella cause?</h3>
                                <p>Most of the time, Salmonella infection leads to gastroenteritis, usually causing just an upset stomach, abdominal cramps, and diarrhea. These symptoms can start as soon as six hours after ingesting the bacteria. Typically, symptoms resolve on their own within two to three days.

                                </p>
                                <p>
                                    Some people have such mild symptoms they're barely noticeable. "The classic case might be a college student who eats a burrito from a sketchy place, gets sick for a couple of days, gets better, and doesn't think anything of it," says Dr. Hohmann.
                                </p>
                                <p>Sometimes symptoms are more serious, such as severe abdominal cramping and bloody diarrhea, or unexplained high fever and marked fatigue. These symptoms require a call to your doctor..</p>
