                                    <h4><a href="#">Salmonella is sneaky: Watch out
                                        </a></h4>
                                    <p style="text-align: justify;">what is Salmonella? If you've ever had a run-in with this bacteria, you know it can cause a food-borne illness called salmonellosis, a form of food poisoning. But you may not know that Salmonella bacteria sicken an estimated 1.35 million people and hospitalize 26,500 every year in the United States. What's more, it kills more total people than any other type of food poisoning.

                                        And Salmonella bacteria can be sneaky — not only by triggering unusual complications, but infecting people in startling ways, says Dr. Elizabeth Hohmann, an infectious disease specialist at Massachusetts General Hospital. One patient she treated showed up with an abdominal aortic aneurysm — a dangerous bulge in the lower section of the body's largest artery — that looked infected. Testing revealed a culprit some would find surprising: Salmonella.
