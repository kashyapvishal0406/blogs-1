                                        <h3>Who is especially vulnerable during hot weather? </h3>
                                        <p>High temperatures can affect anyone. But some people — children, outdoor workers, people who are pregnant or have health problems or disabilities, and older people — are more likely to experience harm when temperatures rise. For example.
                                        </p>
                                        <ul class="layout-link">
                                            <li>Young children, especially babies, have less physical capacity to deal with very high temperatures.</li>
                                            <li>People working outdoors may not have access to shade and could be performing physically intensive labor. They need adequate hydration, adequate breaks, and access to a cool space during break time, as OSHA guidelines spell out.</li>
                                            <li>People with chronic medical conditions, such as kidney disease or heart disease, may have difficulty adapting physiologically to hot weather, or may be more susceptible to its health impacts.</li>
                                        </ul>
