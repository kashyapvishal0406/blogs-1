                                <h3>Which weather patterns create dangerous levels of heat?</h3>
                                <p>Dangerous heat is the result of both high temperatures and high humidity, which interfere with our ability to cool off by sweating. In dry areas, extremely hot temperatures can be dangerous on their own.

                                </p>
                                <p>
                                    Danger zones vary across the United States and around the world. But hospital use and deaths rise once we get above threshold temperatures. The threshold varies in different places depending on whether bodies, cultures, and architecture are adapted to heat.
                                </p>
                                <p>For example, here in New England, where some people (particularly those of limited means) may not have access to air conditioning, we see increases in healthcare use and deaths at a lower temperature than in the American South, where people and organizations may be more used to dealing with hot weather.</p>
